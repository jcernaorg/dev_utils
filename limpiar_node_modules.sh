#!/bin/bash
# limpiar_node_modules.sh - Borra node_modules y vendor antiguos
# Uso: ./limpiar_node_modules.sh /var/www/cread_front/releases

RELEASES_DIR=${1:-/var/www/cread_front/releases}

find "$RELEASES_DIR" -type d -name "node_modules" -exec rm -rf {} +
find "$RELEASES_DIR" -type d -name "vendor" -exec rm -rf {} +

echo "Limpieza completada en $RELEASES_DIR"

exit 0 